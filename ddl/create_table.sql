CREATE TABLE book_info (
    title varchar(200),
    rating int,
    category varchar(30),
    upc varchar(30),
    product_type varchar(30),
    price real,
    price_withtax real,
    tax_value real,
    available_number int,
    review_number int,
    date date,
    year varchar(10),
    month varchar(10),
    day varchar(10)
);